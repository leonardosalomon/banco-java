    public ListaConta(ArrayList<Conta> Contas) {
        initComponents();
        this.Contas = Contas;
        
        //Preenchendo tabela
        for (int i = 0; i < Contas.size(); i++) {
            DefaultTableModel val = (DefaultTableModel) jTableContas.getModel();

            int numero = Contas.get(i).getNumero();
            float saldo = Contas.get(i).getSaldo();
            float limite = Contas.get(i).getLimite();;
            String titular = Contas.get(i).getTitulares().get(0).getNome();
            String cpf = Contas.get(i).getTitulares().get(0).getCpf();
            String endereco = Contas.get(i).getTitulares().get(0).getEndereco();

            val.addRow(new Object[]{numero, saldo,limite,titular,cpf,endereco});
        }
        
        jTextFieldTotalDeContas.setText(Integer.toString(Conta.totalDeContas));
    }

