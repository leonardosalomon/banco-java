    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed

        Pessoa aux = (Pessoa) jComboBoxTitu.getSelectedItem();
              
        jTextAreaBanco.append("\n\nTitulares: \n\n");

            for (Pessoa titu :Contas.getTitulares()) {
                jTextAreaBanco.append("Nome: " + titu.getNome() + "   CPF: " + titu.getCpf()
                        + "   Endereço: " + titu.getEndereco() + "\n");
            }
            
            
       
    }//GEN-LAST:event_jButtonListarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed

        Pessoa rm = (Pessoa) jComboBoxTitu.getSelectedItem();

        //Remove a Conta
        titu.remove(rm);

        //Remove todos os itens do ComboBox
        jComboBoxTitu.removeAllItems();

        //Preenche os ComboBox novamente com as informações pós exclusões
        for (Pessoa aux : titu) {
            jComboBoxTitu.addItem(aux);
        }

    }//GEN-LAST:event_jButtonRemoverActionPerformed

