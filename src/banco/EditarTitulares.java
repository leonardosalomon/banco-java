    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
              
        String nome = (jTextFieldNome.getText());
        String cpf = (jFormattedTextFieldCpf.getText());
        String endereco = (jTextFieldEndereco.getText());

        if (!jTextFieldNome.getText().isEmpty() && !jFormattedTextFieldCpf.getText().isEmpty() && !jTextFieldEndereco.getText().isEmpty()) {

            Pessoa Titular = (Pessoa) jComboBoxTitu.getSelectedItem();

            Titular.setNome(nome);
            Titular.setCpf(cpf);
            Titular.setEndereco(endereco);

            jButtonListar.doClick();
            
            jTextFieldNome.setText("");
            jFormattedTextFieldCpf.setText("");
            jTextFieldEndereco.setText("");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

